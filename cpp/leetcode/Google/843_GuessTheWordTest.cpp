int Master::guess(string word) {
    ++callCnt_;
    size_t res = 0;
    string correctWord = "hbaczn";
    for (size_t i = 0; i < 6; ++i) {
        if (correctWord[i] == word[i])
            ++res;
    }
    return res;
}

